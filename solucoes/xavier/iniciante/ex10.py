"""
Exercício 10 — Soma de valores

Crie uma lista com números inteiros.
Calcule e imprima a soma de todos os valores.
"""

# Dica: use um laço ou a função sum()

numeros = [1, 2, 3, 4, 5]
soma = 0
for numero in numeros:
    soma += numero  
print("A soma dos valores é:", soma)


